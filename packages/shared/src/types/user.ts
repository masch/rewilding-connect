import { z } from "zod";
import { UserRoleSchema } from "./common";

/**
 * Unified User Entity - matches DER §8.1
 *
 * Single table for all user types (tourist, entrepreneur, admin)
 * - Tourist: alias is required, email is null
 * - Entrepreneur/Admin: email is required, alias is null
 */
export const UserSchema = z.object({
  id: z.string().uuid(),
  alias: z.string().max(50).nullable(), // Tourists only
  email: z.string().email().nullable(), // Admin/Entrepreneur only
  first_name: z.string().max(100).nullable(),
  last_name: z.string().max(100).nullable(),
  whatsapp: z.string().nullable(),
  user_type: UserRoleSchema,
  failed_login_attempts: z.number().int().default(0),
  locked_until: z.date().nullable(),
  last_login_at: z.date().nullable(),
  is_active: z.boolean().default(true),
  created_at: z.date(),
});

// TypeScript interface inferred from schema
export type User = z.infer<typeof UserSchema>;

/**
 * Schema for creating a new user (mobile login for tourists)
 * - id and timestamps are generated by the backend
 */
export const CreateUserSchema = UserSchema.omit({
  id: true,
  failed_login_attempts: true,
  locked_until: true,
  last_login_at: true,
  is_active: true,
  created_at: true,
});

export type CreateUserInput = z.infer<typeof CreateUserSchema>;

/**
 * Schema for updating user profile
 */
export const UpdateUserSchema = CreateUserSchema.partial();

export type UpdateUserInput = z.infer<typeof UpdateUserSchema>;
